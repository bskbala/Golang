package main

import (
	"fmt"
)

// Understanding the Type Casting in division Operations

func main() {

	fmt.Println("10/3 =", 10/3)
	fmt.Println("10/3.0", 10/3.0)
	fmt.Println()

	fmt.Println("10 % 3 =", 10%3)
	// fmt.Println("10 % 3.0 =", 10%3.0)
	fmt.Println()

	fmt.Println("5.0/4.0 =", 5.0/4.0)
	fmt.Println("5/4 =", 5/4)
	fmt.Println()

	fmt.Println("-5/3 =", -5/3)
	fmt.Println("-5/-3", -5/-3)
	fmt.Println()

}
